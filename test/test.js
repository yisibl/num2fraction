var test = require('tape')
var num2fraction = require('../')
var π = Math.PI

test('Must be equal', function(t) {
  t.equal(num2fraction(1/0), '0')
  t.equal(num2fraction(0), '0')
  t.equal(num2fraction(.2), '1/5')
  t.equal(num2fraction(1.1), '11/10')
  t.equal(num2fraction(-1.1), '-11/10')
  t.equal(num2fraction(1.2), '6/5')
  t.equal(num2fraction(1.3), '13/10')
  t.equal(num2fraction(1.4), '7/5')
  t.equal(num2fraction(1.5), '3/2')
  t.equal(num2fraction(2), '2/1')
  t.equal(num2fraction(2.1), '21/10')
  t.equal(num2fraction(3), '3/1')
  t.equal(num2fraction(2.555), '511/200')
  t.equal(num2fraction(8.36), '209/25')
  t.equal(num2fraction(-8.36), '-209/25')
  t.equal(num2fraction('3em'), '3/1')
  t.equal(num2fraction('1.5px'), '3/2')
  t.equal(num2fraction('0'), '0')
  t.equal(num2fraction('.2'), '1/5')
  t.equal(num2fraction('1.1'), '11/10')
  t.equal(num2fraction('-1.1'), '-11/10')
  t.equal(num2fraction('8.36'), '209/25')
  t.equal(num2fraction(1 / 3), '1/3')
  t.equal(num2fraction(2 / 3), '2/3')
  t.equal(num2fraction(1 / 9), '1/9')
  t.equal(num2fraction(2 / 9), '2/9')
  t.equal(num2fraction(4 / 9), '4/9')
  t.equal(num2fraction(5 / 9), '5/9')
  t.equal(num2fraction(7 / 9), '7/9')
  t.equal(num2fraction(8 / 9), '8/9')
  t.equal(num2fraction((2 * π / 3) / π), '2/3')
  t.equal(num2fraction((8 * 5) / (4 / 2)), '20/1')
  t.equal(num2fraction(512 / 999), '512/999')
  t.equal(num2fraction(5122 / 9999), '5122/9999')
  t.equal(num2fraction(5152 / 9999), '5152/9999')
  t.equal(num2fraction(6152 / 9999), '6152/9999')
  t.equal(num2fraction(1714 / 3333), '1714/3333')
  t.equal(num2fraction(17141 / 33333), '17141/33333')
  t.equal(num2fraction(17146 / 99999), '17146/99999')
  t.equal(num2fraction(99998 / 99999), '99998/99999')
  t.notEqual(num2fraction(99998 / 99999.0001), '99998/99999')
  t.notEqual(num2fraction(99998.0001 / 99999), '99998/99999')
  t.end()
})
